const I18nContext = createContext();

export function I18nProvider({ children }) {
  const [lang, setLang] = useState(() => localStorage.getItem('app_lang') || 'ar');

  useEffect(() => {
    localStorage.setItem('app_lang', lang);
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
  }, [lang]);

  const t = (key, project) => {
    if (project) {
      if (key === 'governmentShare') return project.government_entity || translations[lang]?.governmentShare || key;
      if (key === 'partnerShare') return project.private_partner || translations[lang]?.partnerShare || key;
    }
    return translations[lang]?.[key] || key;
  };
  const isRTL = lang === 'ar';

  const toggleLang = () => setLang(prev => prev === 'ar' ? 'en' : 'ar');

  return (
    <I18nContext.Provider value={{ lang, t, isRTL, toggleLang, setLang }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}